@push('scripts')
<script>
    document.addEventListener('livewire:initialized', () => {
        Livewire.on('login', (data) => {
            supabase.auth.signInWithPassword({
                email: data[0].email,
                password: data[0].password,
            }).then(({ data, error }) => {
                if (error) {
                    @this.set('error', error.message);
                } else {
                    window.location.href = '/dashboard';
                }
            });
        });
    });
</script>
@endpush 